#include <pthread.h>

// Run the program body on a worker thread with a much larger C stack
// than the default 8 MB.  The SD inline-closure-call chain grows the C
// stack one frame per Lua function invocation, and SD frames can be
// ~500 bytes each for non-trivial functions — deeply recursive programs
// like ack(3, 8) blow through 8 MB easily.  64 MB is plenty for the
// programs we care about and is well below typical address-space
// limits.
typedef struct { int (*entry)(void *); void *arg; int rc; } luastro_thread_arg;
static void *luastro_thread_main(void *p) {
    luastro_thread_arg *a = (luastro_thread_arg *)p;
    a->rc = a->entry(a->arg);
    return NULL;
}
struct run_args { CTX *c; struct ParsedChunk *pc; };
static LuaValue run_chunk(CTX *c, struct ParsedChunk *pc);

static int
luastro_run_chunk_thunk(void *p)
{
    struct run_args *a = (struct run_args *)p;
    (void)run_chunk(a->c, a->pc);
    return 0;
}

static int
luastro_run_on_big_stack(int (*entry)(void *), void *arg)
{
    pthread_attr_t at; pthread_attr_init(&at);
    pthread_attr_setstacksize(&at, 4ULL * 1024 * 1024 * 1024);  // 4 GB virtual
    luastro_thread_arg ta = { entry, arg, 0 };
    pthread_t tid;
    if (pthread_create(&tid, &at, luastro_thread_main, &ta) != 0) {
        pthread_attr_destroy(&at);
        return entry(arg);   // fall back: run on main stack
    }
    pthread_attr_destroy(&at);
    pthread_join(tid, NULL);
    return ta.rc;
}

