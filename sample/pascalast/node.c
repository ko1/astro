#ifndef NODE_SPECIALIZED_INCLUDED
// Empty initial table.  Populated by `pascalast -c FILE.pas`.
static struct specialized_code sc_entries[] = {{0}};
#define SC_ENTRIES_COUNT 0
#endif

void
INIT(void)
{
    GC_init();

    sc_repo.size = SC_ENTRIES_COUNT;
    sc_repo.capa = (sc_repo.size == 0) ? 16 : sc_repo.size * 2;
    sc_repo.entries = malloc(sc_repo.capa * sizeof(struct specialized_code));
    if (sc_repo.size > 0) {
        memcpy(sc_repo.entries, sc_entries, sc_repo.size * sizeof(struct specialized_code));
    }
}
